package se.ErikKnöös.arrays;

public class ifelse {
    public static void main(String[] args) {
        String name = "Erik";

        if(name != ("Erik")){
            System.out.println("Namnet är inte korrekt");
        }else if(name.equals("Erik")){
            System.out.println("Namnet är korrekt");
        }

    }

}
