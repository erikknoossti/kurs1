package se.erik.scanner;

import java.util.Scanner;

public class scanner {

    private int a;
    private int b;


    public scanner(int a, int b){
        this.a = a;
        this.b = b;

    }

    public int add(){ return a + b;}
    public int sub(){ return a - b;}
    public int mul(){ return a * b;}
    public int div(){ return a / b;}




}
